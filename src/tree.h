#ifndef TREE_H
#define TREE_H

#include <array>
#include <vector>
#include <memory>
#include "simulation_setup.h"
#include "topology_manager.h"
#include "block.h"

//No way around this Forward declaration
// If you find one - tell me, please!
class Node;

/**
 * @brief The Tree class holds the information about local fluid data. Data on the MR levels is stored in Node containers as Binary-, Quad-, Oct-tree
 *        in one, two and three dimensions, respectively. Tree does not manipulate the data itself, it is an accessor that provides data to a
 *        solver or similar. No real tree-searches are performed due to the unique node indexing. Tree is rather a gurantee to access the correct
 *        data and that changes are transferable e.g. to the TopologyManager in the proper way. The tree must not change global data at any time.
 */
class Tree {

  const SimulationSetup& setup_;
  const TopologyManager& topology_;

  std::array<std::vector<std::shared_ptr<Node>>, CC::AMNL()> nodes_;

  void InsertNode(const std::uint64_t id, const std::vector<MaterialName> materials);

 public:
    Tree() = delete;
    Tree(const SimulationSetup& setup, const TopologyManager& topology);

    void CreateNode(const std::uint64_t id, Block& block);
    void RemoveNodeWithId(const std::uint64_t id);

    std::vector<std::shared_ptr<Node>> Leaves() const;
    std::vector<std::shared_ptr<Node>> LeavesOnLevel(const unsigned int level) const;

    std::array<std::uint64_t,CC::NOC()> RefineNode(const std::uint64_t id);
    std::shared_ptr<Node> GetNodeWithId(const std::uint64_t id) const;

    const std::vector<std::shared_ptr<Node>>& NodesOnLevel(const unsigned int level) const;

    /**
     * @brief Gives a reference to the complete node list in this tree instance, i e. the complete tree on current MPI rank.
     * @return List of nodes. An array for each level holding arbitrary number of nodes on each level.
     */
    inline const std::array<std::vector<std::shared_ptr<Node>>, CC::AMNL()>& FullNodeList() const {return nodes_;}
};

#endif // TREE_H
