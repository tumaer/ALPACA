#ifndef WATERLIKE_FLUID_H
#define WATERLIKE_FLUID_H

#include "materials/material.h"
#include "materials/material_names.h"
#include "cell.h"
#include <vector>

/**
 * @brief The WaterlikeFluid class implements a generic waterlike fluid, i.e. material parameters must be set via caller input.
 */
class WaterlikeFluid : public Material {

  const double gamma_;
  const double A_;
  const double B_;
  const double rho0_;
  const double mu_shear_;
  const double mu_bulk_;

  public:
    WaterlikeFluid(const MaterialName name, const double gamma, const double A, const double B, const double rho0, const double mu_shear, const double mu_bulk);
    
    double GetPressure(const Cell &cell) const;
    double GetPressure(const double density, const double momentum_x, const double momentum_y, const double momentum_z, const double energy) const;
    
    double GetEnthalpy(const Cell &cell) const;
    double GetEnthalpy(const double density, const double momentum_x, const double momentum_y, const double momentum_z, const double energy) const;
    
    double GetGamma() const;
    double GetA() const;
    double GetB() const;
    double GetF(const Cell &cell) const;
    double GetRoeB1(const double enthalpy_roe_ave, const double q_squared) const;
    double GetRoeB2(const double enthalpy_roe_ave, const double q_squared) const;
    double GetRoeSpeedOfSound(const double enthalpy_roe_ave, const double q_squared, const double rho_left, const double rho_right) const;
    double GetSpeedOfSound(const Cell &cell) const;
    std::vector<double> GetViscosity() const;

    double GetEnergy(const double density, const double momentum_x, const double momentum_y, const double momentum_z, const double pressure) const;

    double GetSpeedOfSound(const double density, const double pressure) const;


};

#endif // WATERLIKE_FLUID_H
