#include "runge_kutta_2_TVD.h"
#include "simulation_setup.h"
#include <iostream>
#include <numeric>


/**
 * @brief Constructor.
 * @param start_time Time when the simulation should start.
 */
RungeKutta2TVD::RungeKutta2TVD(const double start_time) : TimeIntegrator(start_time) {}

/**
 * @brief See base class. Adjusted to Runge-Kutta integration.
 */
void RungeKutta2TVD::IntegrateNode(const std::shared_ptr<Node> &node, const unsigned int stage, const unsigned int amount_of_timesteps) const {

  if(stage >= NumberOfStages()) {
    throw std::invalid_argument("Stage is too large for Runge-Kutta-2 integration");
  }

  double timestep = std::accumulate(micro_timestep_sizes_.crbegin(),micro_timestep_sizes_.crbegin()+amount_of_timesteps,0.0);

  if(stage == NumberOfStages() - 1) {
    timestep *= 0.5;
    IntegrateJumpConservatives(node->GetBlock(),timestep);
  }

  Integrate(node->GetBlock(),timestep);

}

/**
 * @brief See base class.
 * @return Two, always.
 */
unsigned int RungeKutta2TVD::NumberOfStages() const {
  return 2;
}

/**
 * @brief See base class. Adjusted to Runge-Kutta integration.
 */
void RungeKutta2TVD::IntegrateJumpHalos(const std::shared_ptr<Node> &node, const unsigned int stage, const unsigned int amount_of_timesteps) const {

  if(stage >= NumberOfStages()) {
    throw std::invalid_argument("Stage is too large for Runge-Kutta-2 integration");
  }

  double timestep = std::accumulate(micro_timestep_sizes_.crbegin(),micro_timestep_sizes_.crbegin()+amount_of_timesteps,0.0);

  if(stage == NumberOfStages() - 1) {
    timestep *= 0.5;
  }

  IntegrateHalo(node->GetBlock(),timestep);

}
