#ifndef RIEMANN_SOLVER_H
#define RIEMANN_SOLVER_H

#include "block.h"
#include "node.h"
#include "stencils/stencil.h"

#include <type_traits>
#include <memory>

/**
 * @brief Interface to solve the Euler Equations. Uses Stencils (of T-/W-ENO type) to approximate the solution.
 */
template<class S>
class RiemannSolver {

  static_assert(std::is_base_of<Stencil,S>::value, "Template Parameter 'S' must be derived from (abstract) Stencil");

  protected:
    S stencil_;

    /* ATTENTION: order of entries in conservatives differs in this code to the order used in the standard derivations (here: energy as second entry).
     * Hence, use index to loop through the conservatives in the order of derivations and keep all other entries
     */

    //initialization for 1D, 2D, 3D; does not compile if arrays do not have the same lenght, hence padded with -1s
    static constexpr std::array<std::array<int,5>, 3> index_  = { {
                                                                              { {CC::ID_RHO(), CC::ID_XMOM(), CC::ID_ENERGY(),            -1,             -1} },
                                                                              { {CC::ID_RHO(), CC::ID_XMOM(), CC::ID_YMOM(),  CC::ID_ENERGY(),            -1} },
                                                                              { {CC::ID_RHO(), CC::ID_XMOM(), CC::ID_YMOM(),    CC::ID_ZMOM(), CC::ID_ENERGY()} } } };
  public:
    /**
     * @brief Empty virtual destructor.
     */
    virtual ~RiemannSolver() {}
    /**
     * @brief Solves the right hand side of the Euler Equations within the provided node.
     * @param node Container holding the relevant fluid data to compute the update.
     */
    virtual void Update(const std::shared_ptr<Node>& node) const = 0;
};

template<class S> constexpr std::array<std::array<int,5>, 3> RiemannSolver<S>::index_;

#endif // RIEMANN_SOLVER_H
